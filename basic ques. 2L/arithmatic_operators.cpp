#include<iostream>
using namespace std;
int main ()
{
    int a = 10;
    int b = 5;
    cout<<"a+b : "<<a+b<<endl;//addition
    cout<<"a-b : "<<a-b<<endl;//subtraction 
    cout<<"a*b : "<<a*b<<endl;//multiplication
    cout<<"a%b : "<<a%b<<endl;//modula
    cout<<"a/b : "<<a/b<<endl;//division
    cout<<"a : "<<a++<<endl;
    cout<<"now a : "<<a++<<endl;//increment
     cout<<"b : "<<a--<<endl;
    cout<<"now b : "<<a--<<endl;//increment
    
    
    
    return 0;
}